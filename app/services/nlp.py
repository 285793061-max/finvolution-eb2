def _analyze_sentiment(text: str) -> tuple[SentimentLabel, float]:
    text_lower = text.lower()
    pos_count = sum(1 for w in POSITIVE_WORDS if w in text)
    neg_count = sum(1 for w in NEGATIVE_WORDS if w in text)

    if pos_count == 0 and neg_count == 0:
        return "neutral", 0.0

    score = (pos_count - neg_count) / (pos_count + neg_count + 1)
    score = max(-1.0, min(1.0, score))

    if neg_count > pos_count:
        label: SentimentLabel = "negative"
    elif pos_count > neg_count:
        label = "positive"
    else:
        label = "neutral"

    return label, score


def _detect_dimensions(text: str) -> list[str]:
    text_lower = text.lower()
    dimensions: list[str] = []

    for dim, keywords in DIMENSION_DICT.items():
        if any(k.lower() in text_lower for k in keywords):
            dimensions.append(dim)

    return dimensions


def analyze_items(items: List[RawItem]) -> List[ItemSentiment]:
    analyzed: list[ItemSentiment] = []

    for item in items:
        label, score = _analyze_sentiment(item.text)
        dims = _detect_dimensions(item.text)

        analyzed.append(
            ItemSentiment(
                id=item.id,
                text=item.text,
                source_type=item.source_type,
                created_at=item.created_at,
                sentiment_label=label,
                sentiment_score=score,
                dimensions=dims,
            )
        )

    return analyzed

